/// Enumerate one tile's packets in **resolution level-layer-component-
/// position** (RLCP) progression order per T.800 §B.12.1.2.
///
/// `layers` is `L` — the number of quality layers signalled by the `COD`
/// marker (T.800 §A.6.1 / Table A.14). `components` carries one
/// [`ComponentProgressionInfo`] per component, in `Csiz`-declaration order.
///
/// The returned vector lists every packet `(r, l, i, k)` with
/// `r ∈ 0..=Nmax`, `l ∈ 0..L`, `i ∈ 0..components.len()`,
/// `k ∈ 0..numprecincts(r, i)`, skipping any `(r, l, i, *)` for which
/// `r > NL_i` (the §B.12 NOTE rule). `Nmax = max_i(NL_i)`.
///
/// # Loop order (verbatim per §B.12.1.2)
///
/// ```text
/// for each r = 0..=Nmax
///   for each l = 0..L
///     for each i = 0..Csiz
///       for each k = 0..numprecincts(r, i)
/// ```
///
/// RLCP differs from [`lrcp_packet_order`] only in the position of the
/// `r` and `l` loops: LRCP iterates layer-first (every layer's worth of
/// resolution levels appears together), RLCP iterates resolution-first
/// (every resolution level's worth of layers appears together). The
/// inner two loops (`i`, `k`), the `Nmax` definition, the §B.12 NOTE
/// rule on components with `NL_i < r`, and the §B.6 / §B.9 rule on
/// empty precincts still producing packets are all identical.
///
/// # Errors
///
/// * [`Error::InvalidPacketHeader`] if any
///   [`ComponentProgressionInfo::validate`] check fails (per-component
///   `precincts_per_resolution` length mismatch).
/// * [`Error::InvalidComponentCount`] if `components.is_empty()`
///   (T.800 Table A.9 / §A.5: `Csiz` is constrained to `1..=16384`).
///
/// # Empty-corner behaviour
///
/// * `layers == 0` — returns an empty `Vec` (the inner `l`-loop runs
///   `0..0`, so every `r` contributes nothing).
/// * A component with `NL_i = 0` and `precincts_per_resolution = [0]`
///   contributes zero packets — its innermost `k`-loop runs `0..0`.
pub fn rlcp_packet_order(
    layers: u16,
    components: &[ComponentProgressionInfo],
) -> Result<Vec<PacketDescriptor>, Error> {
    if components.is_empty() {
        return Err(Error::InvalidComponentCount);
    }
    for ci in components {
        ci.validate()?;
    }
    if layers == 0 {
        return Ok(Vec::new());
    }

    // Nmax = max NL_i across all components. The r-loop runs 0..=Nmax;
    // components with NL_i < r contribute nothing at that r.
    let n_max = components
        .iter()
        .map(|c| c.num_decomposition_levels)
        .max()
        .unwrap_or(0);

    // Total packet count is invariant under r↔l swap, so the LRCP
    // estimator gives the correct capacity hint here too.
    let cap = estimate_packet_count(layers, n_max, components);
    let mut out = Vec::with_capacity(cap);

    for r in 0..=n_max {
        for l in 0..layers {
            for (i, ci) in components.iter().enumerate() {
                if r > ci.num_decomposition_levels {
                    continue;
                }
                let n_pre = ci.precincts_at(r);
                for k in 0..n_pre {
                    out.push(PacketDescriptor {
                        layer: l,
                        resolution: r,
                        component: i as u16,
                        precinct: k,
                    });
                }
            }
        }
    }
    Ok(out)
}

/// Saturating estimate of total packet count for the LRCP / RLCP loop
/// (the two orders enumerate the same set of `(l, r, i, k)` tuples and
/// therefore have identical totals — only the order in which they're
/// emitted differs):
/// `L * sum_r sum_i numprecincts(r, i)` where the `r`-sum runs over
/// `0..=NL_i`. Used only as a `Vec::with_capacity` hint.
fn estimate_packet_count(layers: u16, n_max: u8, components: &[ComponentProgressionInfo]) -> usize {
    let mut per_layer: usize = 0;
    for r in 0..=n_max {
        for ci in components {
            if r > ci.num_decomposition_levels {
                continue;
            }
            per_layer = per_layer.saturating_add(ci.precincts_at(r) as usize);
        }
    }
    per_layer.saturating_mul(layers as usize)
}

// ===========================================================================
// Position-keyed orders — T.800 §B.12.1.3 RPCL / §B.12.1.4 PCRL /
// §B.12.1.5 CPRL.
//
// These three orders interleave packets by **reference-grid position**
// rather than per-(resolution, component) raster index. The spec writes
// them as a `for y / for x` sweep over the tile's reference grid, but the
// §B.12.1.3 NOTE permits (and recommends) computing each precinct's
// reference-grid top-left corner directly and ordering the visits by that
// corner. That is what the machinery below does.
// ===========================================================================

/// Per-resolution-level precinct layout for one component, with the
/// reference-grid mapping needed by the position-keyed orders.
///
/// The position-keyed orders (RPCL / PCRL / CPRL) need each precinct's
/// **top-left corner on the reference grid** to order the visits. Per
/// T.800 §B.6 the precinct partition of resolution level `r` is anchored
/// at `(0, 0)` on the reduced-resolution tile-component domain with step
/// `(2^PPx, 2^PPy)`; precinct cell `(px, py)` therefore has its left
/// edge at reduced-resolution coordinate
/// `max((floor(trx0 / 2^PPx) + px) · 2^PPx, trx0)` (top edge analogous on
/// `y`). Equation B-20 maps a reduced-resolution coordinate back to the
/// reference grid by multiplying by `XRsiz(i) · 2^(NL_i − r)` (the
/// resolution-level scale times the component sub-sampling), giving the
/// `XRsiz(i) · 2^(PPx + NL_i − r)` reference-grid precinct step the spec's
/// divisibility conditions test against.
///
/// One [`ResolutionPrecinctLayout`] is held per resolution level `r =
/// 0..=NL_i` of the component, in `r` order.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct ResolutionPrecinctLayout {
    /// `numprecinctswide` for this resolution level (T.800 Equation B-16,
    /// from [`crate::geometry::PrecinctPartition::num_wide`]). The
    /// precinct grid is `num_wide × num_high`; the raster precinct index
    /// `k` decomposes as `px = k % num_wide`, `py = k / num_wide`.
    pub num_wide: u32,
    /// `numprecinctshigh` for this resolution level
    /// ([`crate::geometry::PrecinctPartition::num_high`]).
    pub num_high: u32,
    /// `floor(trx0 / 2^PPx)` — the precinct-partition anchor cell index on
    /// the reduced-resolution domain's x-axis (§B.6: the partition starts
    /// at `(0, 0)` and the resolution level's left edge `trx0` falls in
    /// anchor cell `floor(trx0 / 2^PPx)`). The reference-grid corner of
    /// precinct column `px` uses `anchor_x + px`.
    pub anchor_x: u32,
    /// `floor(try0 / 2^PPy)` — the partition anchor cell index on the
    /// reduced-resolution domain's y-axis.
    pub anchor_y: u32,
    /// `trx0` — the resolution level's left edge on the reduced-resolution
    /// tile-component domain (T.800 Equation B-14). The first precinct
    /// column's reference-grid corner is clipped up to this edge.
    pub trx0: u32,
    /// `try0` — the resolution level's top edge on the reduced-resolution
    /// tile-component domain.
    pub try0: u32,
    /// `PPx` — precinct width exponent in force at this resolution level
    /// ([`crate::geometry::PrecinctExponents::ppx`]).
    pub ppx: u8,
    /// `PPy` — precinct height exponent in force at this resolution level.
    pub ppy: u8,
}

impl ResolutionPrecinctLayout {
    /// `numprecincts = num_wide · num_high` (T.800 §B.6).
    pub fn num_precincts(&self) -> u64 {
        self.num_wide as u64 * self.num_high as u64
    }

    /// Reference-grid x-coordinate of the left edge of precinct column
    /// `px` (`0..num_wide`) at this resolution level, given the
    /// component's `NL_i` and sub-sampling `XRsiz(i)`.
    ///
    /// Per §B.6 / Equation B-20: the precinct cell's reduced-resolution
    /// left edge is `max((anchor_x + px) · 2^PPx, trx0)`; multiplying by
    /// `XRsiz(i) · 2^(NL_i − r)` projects it to the reference grid. The
    /// result is the value the §B.12.1.3 `for x` loop reaches when the
    /// divisibility condition first fires for this precinct column —
    /// i.e. the precinct column's ordering key on the x-axis.
    fn ref_grid_x(&self, r: u8, n_l: u8, xrsiz: u8, px: u32) -> u64 {
        let cell_left = ((self.anchor_x as u64) + (px as u64)) << (self.ppx as u64);
        let clipped = cell_left.max(self.trx0 as u64);
        let scale = (xrsiz as u64) << ((n_l.saturating_sub(r)) as u64);
        clipped.saturating_mul(scale)
    }

    /// Reference-grid y-coordinate of the top edge of precinct row `py`
    /// (`0..num_high`). The y-axis analogue of [`Self::ref_grid_x`].
    fn ref_grid_y(&self, r: u8, n_l: u8, yrsiz: u8, py: u32) -> u64 {
        let cell_top = ((self.anchor_y as u64) + (py as u64)) << (self.ppy as u64);
        let clipped = cell_top.max(self.try0 as u64);
        let scale = (yrsiz as u64) << ((n_l.saturating_sub(r)) as u64);
        clipped.saturating_mul(scale)
    }
}

/// Per-component input to the position-keyed (RPCL / PCRL / CPRL)
/// progression-order drivers.
///
/// In addition to the decomposition depth `NL_i` the position-keyed
/// orders need, per resolution level, the precinct grid plus the
/// reference-grid mapping (see [`ResolutionPrecinctLayout`]) and the
/// component's sub-sampling factors `XRsiz(i)` / `YRsiz(i)` from the SIZ
/// marker (T.800 Table A.9). The layouts are indexed by resolution level
/// `r = 0..=NL_i` and must contain exactly `NL_i + 1` entries.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct ComponentPositionInfo {
    /// `NL_i` — number of wavelet decomposition levels for this component
    /// (`COD` / `COC`). Resolution levels run `r = 0..=NL_i`.
    pub num_decomposition_levels: u8,
    /// `XRsiz(i)` — horizontal sub-sampling factor (T.800 Table A.9,
    /// `1..=255`).
    pub xrsiz: u8,
    /// `YRsiz(i)` — vertical sub-sampling factor (`1..=255`).
    pub yrsiz: u8,
    /// One [`ResolutionPrecinctLayout`] per resolution level
    /// `r = 0..=NL_i`, indexed by `r`. Length must equal
    /// `num_decomposition_levels + 1`.
    pub resolutions: Vec<ResolutionPrecinctLayout>,
}

impl ComponentPositionInfo {
    /// Validates the per-component invariants:
    ///
    /// * `resolutions.len() == num_decomposition_levels + 1`
    /// * `xrsiz != 0` and `yrsiz != 0` (Table A.9: sub-sampling factors
    ///   are `1..=255`; a zero would make the reference-grid scale
    ///   collapse and is malformed by construction).
    ///
    /// Returns [`Error::InvalidPacketHeader`] on a length mismatch and
    /// [`Error::InvalidComponentCount`] on a zero sub-sampling factor
    /// (the latter reuses the component-shape error since a degenerate
    /// component is what it signals).
    pub fn validate(&self) -> Result<(), Error> {
        if self.resolutions.len() != self.num_decomposition_levels as usize + 1 {
            return Err(Error::InvalidPacketHeader);
        }
        if self.xrsiz == 0 || self.yrsiz == 0 {
            return Err(Error::InvalidComponentCount);
        }
        Ok(())
    }

    /// The number of precincts at resolution level `r` (`0` past the
    /// component's top resolution level — the §B.12 NOTE rule).
    fn precincts_at(&self, r: u8) -> u64 {
        if r > self.num_decomposition_levels {
            0
        } else {
            self.resolutions
                .get(r as usize)
                .map(|l| l.num_precincts())
                .unwrap_or(0)
        }
    }
}

/// One precinct's identity plus its reference-grid corner, used to order
/// the position-keyed sweeps. The `(y, x)` corner is the primary sort
/// key (y-major, then x — matching the spec's `for y / for x` nesting);
/// `(resolution, component, precinct)` break ties deterministically and
/// carry the descriptor coordinates.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
struct PrecinctVisit {
    /// Reference-grid top edge (`y`) of the precinct — primary sort key.
    ref_y: u64,
    /// Reference-grid left edge (`x`) of the precinct — secondary key.
    ref_x: u64,
    resolution: u8,
    component: u16,
    precinct: u32,
}

/// Build the ordered precinct-visit list for the position-keyed orders.
///
/// `key_order` selects how the visits are sorted, which is the *only*
/// thing that distinguishes RPCL / PCRL / CPRL once the precinct corners
/// are known:
///
/// * RPCL (§B.12.1.3): resolution → `y` → `x` → component.
/// * PCRL (§B.12.1.4): `y` → `x` → component → resolution.
/// * CPRL (§B.12.1.5): component → `y` → `x` → resolution.
///
/// In every order the innermost loop after a precinct is fixed is the
/// layer loop `l = 0..L`, handled by the caller. Within a fixed outer
/// nesting the `for x` loop is inside `for y`, and within a fixed
/// `(y, x)` the component loop (RPCL/PCRL) or resolution loop
/// (PCRL/CPRL) follows the spec's stated order.
fn ordered_precinct_visits(
    components: &[ComponentPositionInfo],
    key_order: PositionKey,
) -> Vec<PrecinctVisit> {
    // Collect every precinct of every (resolution, component) with its
    // reference-grid corner.
    let mut visits: Vec<PrecinctVisit> = Vec::new();
    for (i, ci) in components.iter().enumerate() {
        let n_l = ci.num_decomposition_levels;
        for r in 0..=n_l {
            let layout = match ci.resolutions.get(r as usize) {
                Some(l) => l,
                None => continue,
            };
            if layout.num_wide == 0 || layout.num_high == 0 {
                continue;
            }
            for py in 0..layout.num_high {
                let ref_y = layout.ref_grid_y(r, n_l, ci.yrsiz, py);
                for px in 0..layout.num_wide {
                    let ref_x = layout.ref_grid_x(r, n_l, ci.xrsiz, px);
                    let k = py * layout.num_wide + px;
                    visits.push(PrecinctVisit {
                        ref_y,
                        ref_x,
                        resolution: r,
                        component: i as u16,
                        precinct: k,
                    });
                }
            }
        }
    }

    // Stable sort by the order-specific key. A stable sort preserves the
    // collection order (resolution-then-component-then-raster) for any
    // tuples that compare equal on the chosen key, which keeps the
    // tie-break deterministic and matches the spec's nested-loop visiting.
    match key_order {
        // RPCL: outer r, then position (y, x), then component.
        PositionKey::Rpcl => {
            visits.sort_by_key(|v| (v.resolution, v.ref_y, v.ref_x, v.component, v.precinct))
        }
        // PCRL: outer position (y, x), then component, then resolution.
        PositionKey::Pcrl => {
            visits.sort_by_key(|v| (v.ref_y, v.ref_x, v.component, v.resolution, v.precinct))
        }
        // CPRL: outer component, then position (y, x), then resolution.
        PositionKey::Cprl => {
            visits.sort_by_key(|v| (v.component, v.ref_y, v.ref_x, v.resolution, v.precinct))
        }
    }
    visits
}

/// Selects the precinct-visit ordering for [`ordered_precinct_visits`].
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
enum PositionKey {
    Rpcl,
    Pcrl,
    Cprl,
}

/// Shared driver: validate inputs, build the ordered precinct visits for
/// `key`, then expand each visit into `L` layer descriptors (layer is the
/// innermost loop in all three position orders).
fn position_packet_order(
    layers: u16,
    components: &[ComponentPositionInfo],
    key: PositionKey,
) -> Result<Vec<PacketDescriptor>, Error> {
    if components.is_empty() {
        return Err(Error::InvalidComponentCount);
    }
    for ci in components {
        ci.validate()?;
    }
    if layers == 0 {
        return Ok(Vec::new());
    }

    let visits = ordered_precinct_visits(components, key);

    // Capacity: total precincts across all (r, i) times L. Saturating so
    // a hostile input can't overflow the multiply.
    let total_precincts: usize = components
        .iter()
        .map(|ci| {
            let mut s: usize = 0;
            for r in 0..=ci.num_decomposition_levels {
                s = s.saturating_add(ci.precincts_at(r) as usize);
            }
            s
        })
        .fold(0usize, |a, b| a.saturating_add(b));
    let cap = total_precincts.saturating_mul(layers as usize);
    let mut out = Vec::with_capacity(cap);

    for v in &visits {
        for l in 0..layers {
            out.push(PacketDescriptor {
                layer: l,
                resolution: v.resolution,
                component: v.component,
                precinct: v.precinct,
            });
        }
    }
    Ok(out)
}

