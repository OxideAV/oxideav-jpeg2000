/// Enumerate one tile's packets in **resolution level-position-component-
/// layer** (RPCL) progression order per T.800 §B.12.1.3.
///
/// `layers` is `L` — the number of quality layers (`COD`); `components`
/// carries one [`ComponentPositionInfo`] per component in
/// `Csiz`-declaration order.
///
/// # Loop order (per §B.12.1.3)
///
/// ```text
/// for each r = 0..=Nmax
///   for each y = ty0..ty1          // reference-grid position
///     for each x = tx0..tx1
///       for each i = 0..Csiz
///         for the next precinct k at (x, y), if one exists
///           for each l = 0..L
/// ```
///
/// The `(y, x)` sweep is the spec's expository form; per the §B.12.1.3
/// NOTE this implementation visits precinct corners directly (computed
/// via [`ResolutionPrecinctLayout::ref_grid_x`] / `ref_grid_y`) ordered
/// `resolution → y → x → component`, then emits all `L` layers per
/// precinct. The result is identical to the literal `(y, x)` walk with
/// the empty positions skipped.
///
/// # Errors
///
/// * [`Error::InvalidPacketHeader`] if any
///   [`ComponentPositionInfo::validate`] length check fails.
/// * [`Error::InvalidComponentCount`] if `components.is_empty()` or a
///   component has a zero sub-sampling factor.
///
/// # Empty-corner behaviour
///
/// * `layers == 0` → empty `Vec`.
/// * A resolution level with `num_wide == 0` or `num_high == 0`
///   contributes no precincts (§B.6 degenerate level).
pub fn rpcl_packet_order(
    layers: u16,
    components: &[ComponentPositionInfo],
) -> Result<Vec<PacketDescriptor>, Error> {
    position_packet_order(layers, components, PositionKey::Rpcl)
}

