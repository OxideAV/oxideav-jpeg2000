//! Annex C CxtVLC code tables for the HT cleanup pass (ITU-T T.814 |
//! ISO/IEC 15444-15:2019, Annex C).
//!
//! Each entry is a 7-tuple `(cq, ρ, u_off, ε̄ᵏ, ε̄¹, w, lw)` exactly as
//! specified in the spec's bracket notation. The `decodeCxtVLC`
//! procedure of §7.3.5 imports bits from the VLC bit-stream one at a
//! time, building a little-endian codeword `cwd` of length `len`, and
//! returns the `(ρ, u_off, ε̄ᵏ, ε̄¹)` tuple of the matching entry whose
//! `cq`, `cwd` (= `w`) and `len` (= `lw`) fields all agree.
//!
//! `CXT_VLC_TABLE_0` is used for quads in the first line-pair of a
//! code-block (`q < QW`); `CXT_VLC_TABLE_1` for every other quad.
//!
//! Transcribed verbatim from `docs/image/jpeg2000/` (T.814 Annex C).

/// One CxtVLC code-table entry (T.814 Annex C).
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub(crate) struct CxtVlcEntry {
    /// Coding context `cq` (0..=7) the entry applies to.
    pub(crate) cq: u8,
    /// Significance pattern `ρ_q` (4-bit) revealed by the codeword.
    pub(crate) rho: u8,
    /// Unsigned-residual offset `u_off` (0 or 1).
    pub(crate) u_off: u8,
    /// EMB known-bit pattern `ε̄ᵏ_q` (4-bit).
    pub(crate) e_k: u8,
    /// EMB known-1 pattern `ε̄¹_q` (4-bit).
    pub(crate) e_1: u8,
    /// Codeword `w` as a little-endian integer (LSB imported first).
    pub(crate) cwd: u8,
    /// Codeword length `lw` in bits (1..=7).
    pub(crate) len: u8,
}

